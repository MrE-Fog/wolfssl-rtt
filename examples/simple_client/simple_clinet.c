const int root_certificate_len = sizeof(root_certificate);
char read_buff[6 * 1024];

#define SERVER_NAME "www.rt-thread.org"
#define SERVER_PORT   "443"
static const char *REQUEST =
    "GET /download/rt-thread.txt HTTP/1.1\r\n"
    "Host: www.rt-thread.org\r\n"
    "User-Agent: rtthread/3.1 rtt\r\n"
    "Connection:close\r\n"
    "\r\n";

void wolfssl_test_thread(void *param) {
    int sockfd = -1;
    WOLFSSL_CTX *ctx;
    WOLFSSL *ssl;
    WOLFSSL_METHOD *method;
    struct addrinfo hints, *addr_list;
    int recvLen = 0;

    rt_memset(read_buff, 0, sizeof(read_buff));
    rt_memset(&hints,0,sizeof(hints));
    hints.ai_family = AF_INET;
    hints.ai_socktype = SOCK_STREAM;
    hints.ai_protocol = IPPROTO_TCP;

    if (getaddrinfo(SERVER_NAME, SERVER_PORT, &hints, &addr_list) != 0) {
        rt_kprintf("failed to get addr info\n");
        return;
    }

    /*create and set up socket*/
    sockfd = socket(AF_INET, SOCK_STREAM, 0);

    /*connect to socket*/
    connect(sockfd,addr_list->ai_addr, addr_list->ai_addrlen);
    freeaddrinfo(addr_list);

    /*initialize wolfssl library*/
    wolfSSL_Init();
    //wolfSSL_Debugging_ON();
    
    method = wolfTLSv1_2_client_method();  // use TLSV1.2

    /*make new ssl context*/
    if ((ctx = wolfSSL_CTX_new(method)) == NULL) {
        rt_kprintf("wolfSSL_CTX_new error");
    }

    /*鑻ョ郴缁熸棤娉曟彁渚涚湡瀹炴椂闂磋鍘绘帀涓嬮潰鐨勬敞閲婏紝鍚﹀垯璇佷功鏍￠獙浼氬嚭閿�*/
    //wolfSSL_CTX_set_verify(ctx, WOLFSSL_VERIFY_NONE, 0);

    /*make new wolfSSL struct*/
    if ((ssl = wolfSSL_new(ctx)) == NULL) {
        rt_kprintf("wolfSSL_new error");
    }

    /*add cert to ctx*/
    if (wolfSSL_CTX_load_verify_buffer(ctx, (unsigned char *)root_certificate, root_certificate_len,
                                       SSL_FILETYPE_PEM) != SSL_SUCCESS) {
        rt_kprintf("Error loading ca certs");
    }
    /*connect wolfssl to the socket, server then send message*/
    wolfSSL_set_fd(ssl, sockfd);
    wolfSSL_connect(ssl);
    wolfSSL_write(ssl, REQUEST, strlen(REQUEST));

    /*read message*/
    do {
        recvLen = wolfSSL_read(ssl, read_buff, sizeof(read_buff));
        if (recvLen > 0) {
            printf("wolfSSL received:\n%s\n", read_buff);
        }
    } while (recvLen > 0);
    if (recvLen < 0) {
        rt_kprintf("wolfSSL_read error = %d\n", wolfSSL_get_error(ssl, recvLen));
    } else if (recvLen == 0) {
        rt_kprintf("Connection close by peer\n");
    }

    /*free all data before client termination*/
    wolfSSL_free(ssl);
    close(sockfd);
    wolfSSL_CTX_free(ctx);
    wolfSSL_Cleanup();
}

int wolfssl_test(void) {
    rt_thread_t tid = rt_thread_create("tls_test", wolfssl_test_thread, NULL, 10 * 1024, 5, 5);
    if (tid) {
        rt_thread_startup(tid);
    } else {
        return -1;
    }
    return 0;
}
MSH_CMD_EXPORT(wolfssl_test,wolfssl_test);
